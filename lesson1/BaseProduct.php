<?php


class BaseProduct
{
    protected $name;
    protected $price;

    /**
     * baseProduct constructor.
     * @param $name
     * @param $price
     */
    public function __construct($name, $price)
    {
        $this->name = $name;
        $this->price = $price;
        $this->showProduct();
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getPrice()
    {
        return $this->price;
    }

    /**
     * @param mixed $price
     */
    public function setPrice($price)
    {
        $this->price = $price;
    }

    public function showProduct() {
        echo 'Название продукта: ' . $this->getName() . ', стоимость продукта: ' . $this->getPrice() . ';<br>';
    }
}